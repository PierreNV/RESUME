const clear = () => {
  map.boundaries = [];
  map.pellets = [];
  map.powerUps = [];
};

const set = (i) => {
  drafts[i].forEach((row, ri) => {
    row.forEach((symbol, ci) => {
      switch (symbol) {
        case "#":
          map.boundaries.push(
            new Boundary({
              position: {
                x: Boundary.width * ci,
                y: Boundary.height * ri,
              },
              color: "blue",
            })
          );
          break;
        case ":":
          map.boundaries.push(
            new Boundary({
              position: {
                x: Boundary.width * ci,
                y: Boundary.height * ri,
              },
              isDoor: true,
            })
          );
          break;
        case ".":
          map.pellets.push(
            new Pellet({
              position: {
                x: Boundary.width * ci + Boundary.width / 2,
                y: Boundary.height * ri + Boundary.width / 2,
              },
            })
          );
          break;
        case "*":
          map.powerUps.push(
            new PowerUp({
              position: {
                x: Boundary.width * ci + Boundary.width / 2,
                y: Boundary.height * ri + Boundary.width / 2,
              },
            })
          );
          break;
      }
    });
  });
};

const draw = (boundaries) => {
  boundaries.forEach((boundary) => {
    if (!boundary.isDoor) boundary.draw();
  });
};

export let map = { index: 0, boundaries: [], pellets: [], powerUps: [] };

export const createMap = (i) => {
  clear();
  set(i);
  draw(map.boundaries);
};

export const incrementMapIndex = () => {
  if (map.index < Object.keys(drafts).length - 1) {
    map.index += 1;
  }
};

export const resetMapIndex = () => {
  map.index = 0;
};
